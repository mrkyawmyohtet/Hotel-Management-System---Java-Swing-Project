    private void btn_removePActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removePActionPerformed
        // TODO add your handling code here:
        TableModel model = table_listedPackages.getModel();
        int index = table_listedPackages.getSelectedRow();
        String packageid = model.getValueAt(index, 0).toString();
        int result = JOptionPane.showConfirmDialog(null, "Do you really want to remove this package from the booking list?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if(result == JOptionPane.YES_OPTION){
            try{
                Connection con = connect();
                String sql = "delete from reserved_temp where package_id = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, packageid);
                ps.execute();
                JOptionPane.showMessageDialog(null, "The package has been removed from the booking list", "Removed From List", JOptionPane.INFORMATION_MESSAGE);
                display();
                lbl_packageName.setText("Package Name: ");
                lbl_packageType.setText("Package Type: ");
                lbl_packagePrice.setText("Package Price: ");
                lbl_service.setText("Service: ");
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Thank you for choosing our hotel!\nWe appreciate your decision not to cancel your room booking.\nWe look forward to ensuring a delightful stay for you.",
            "Thank You", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_removePActionPerformed

