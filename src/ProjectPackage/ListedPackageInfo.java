    public String[] getID(){
        try{
            Connection con = connect();
            String sql = "Select package_id from reserved_temp";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            // Create a list to store room IDs dynamically
            List<String> idList = new ArrayList<>();

            // Iterate through the result set to retrieve room IDs
            while (rs.next()) {
                String roomid = rs.getString("package_id");
                idList.add(roomid);
            }

            // Convert the list to an array
            String[] ids = idList.toArray(new String[0]);
            return ids;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            return null;
        }     
    }
    
    public void display(){
        String[] packageids = getID();
        DefaultTableModel model = (DefaultTableModel) table_listedPackages.getModel();
        try{
            Connection con = connect();
            model.setRowCount(0);
            String sql = "select package_id, package_name, package_type, package_price, services from packages where package_id = ?";
            for(String id : packageids){
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, id);
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    Object[] dataRow = {
                        rs.getString("package_id"),
                        rs.getString("package_name"),
                        rs.getString("package_type"),
                        rs.getString("package_price"),
                        rs.getString("services")
                    };
                    model.addRow(dataRow);
                }
                table_listedPackages.setModel(model);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

