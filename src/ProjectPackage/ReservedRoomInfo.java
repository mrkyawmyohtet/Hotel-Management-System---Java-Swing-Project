    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)table_reservedDataTemp.getModel();
        int index = table_reservedDataTemp.getSelectedRow();
        String id = model.getValueAt(index, 0).toString();
        try{
            //first confirm the remove
            int result = JOptionPane.showConfirmDialog(null, "Do you really want to remove this room from the booking list?", "Confrim Message", JOptionPane.YES_NO_OPTION);
            //if customer chose yes, remove from the table
            if(result == JOptionPane.YES_OPTION){
                Connection con = connect();
                //first remove the room from the booking list
                String sql = "delete from reserved_temp where room_id = ?";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, id);
                ps.execute();
                
                
                //update the status of the room
                String updateStatus = "update room set status = 'Available' where room_id = ?";
                PreparedStatement updatePs = con.prepareStatement(updateStatus);
                updatePs.setString(1, id);
                updatePs.executeUpdate();
                display();
                lbl_roomNo.setText("");
                lbl_roomType.setText("");
                
                //show a message to the customer
                JOptionPane.showMessageDialog(null, "The room has been removed from the booking list!");                
            }
            else{
                JOptionPane.showMessageDialog(null, "Thank you for choosing our hotel!\nWe appreciate your decision not to cancel your room booking.\nWe look forward to ensuring a delightful stay for you.",
                "Thank You", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_removeActionPerformed

