    public void display()
    {
        String[] ids = getID();
        DefaultTableModel model = (DefaultTableModel)table_reservedDataTemp.getModel();
        try
        {
            model.setRowCount(0);
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = connect();
            String sql = "Select room_id, room_no, room_type, room_price, description, bed from room where room_id = ?";
            for (String id : ids) {
                PreparedStatement pstmt = con.prepareStatement(sql);
                pstmt.setString(1, id);
                ResultSet rs = pstmt.executeQuery();
                while (rs.next()) {
                Object[] rowData = {
                    rs.getString("room_id"),
                    rs.getString("room_no"),
                    rs.getString("room_type"),
                    rs.getString("room_price"),
                    rs.getString("description"),
                    rs.getString("bed")
                };
                model.addRow(rowData);
            }
                table_reservedDataTemp.setModel(model);
           }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
